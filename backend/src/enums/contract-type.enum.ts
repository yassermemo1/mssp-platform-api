/**
 * Contract Type Enumeration
 * Defines the different types of contracts that can be created
 */
export enum ContractType {
  MSA = 'msa', // Master Service Agreement
  SOW = 'sow', // Statement of Work
  AMENDMENT = 'amendment', // Contract Amendment
  RENEWAL = 'renewal', // Contract Renewal
  ADDENDUM = 'addendum', // Contract Addendum
  NDA = 'nda', // Non-Disclosure Agreement
  SLA = 'sla', // Service Level Agreement
  OTHER = 'other', // Other contract types
} 